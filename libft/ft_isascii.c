#include "libft.h"

// last ascii from man
// 0x7f = 0111 1111
// ~0x7f = 1000 0000 kind of equal 0x80 
// which is outside the range of ascii by 1 bit
// but in this scope we looking at int32
// 0x7f int = 0000 0000  0000 0000  0000 0000  0111 1111
// ~0x7f int= 1111 1111  1111 1111  1111 1111  1000 0000
//
// when we compare this with & 
// anything outside the range will result in non zero
// vice versa anything in ascii range will become 0
//
// example with int32
// 0x80 = 0000 0000  0000 0000  0000 0000  1000 0000
// ~0x7f= 1111 1111  1111 1111  1111 1111  1000 0000
// 'K'  = 0000 0000  0000 0000  0000 0000  0100 1011
//      = 0000 0000  0000 0000  0000 0000  0000 0000 = 0

// ~0x7f= 1111 1111  1111 1111  1111 1111  1000 0000
// 4242 = 0000 0000  0000 0000  0001 0000  1001 0010
//      = 0000 0000  0000 0000  0001 0000  1000 0000 = 1 0000 1000 0000

// ~0x7f= 1111 1111  1111 1111  1111 1111  1000 0000
// 256  = 0000 0000  0000 0000  0000 0001  0000 0000
//      = 0000 0000  0000 0000  0000 0001  0000 0000 = 1 0000 0000

// intMAX 0111 1111  1111 1111  1111 1111  1111 1111
// intMIN 1000 0000  0000 0000  0000 0000  0000 0000
//
// -2147483648
// intMIN 1000 0000  0000 0000  0000 0000  0000 0000
// -2147483647
// intMIN 1000 0000  0000 0000  0000 0000  0000 0001
// -2147483646
// intMIN 1000 0000  0000 0000  0000 0000  0000 0010
// -2147483645
// intMIN 1000 0000  0000 0000  0000 0000  0000 0011

// 0 -> 2147483647 -> -2147483648 ->k

// then flip the result so we get the intended isascii
int	ft_isascii(int c)
{
	return (!(c & ~0x7f));
}
